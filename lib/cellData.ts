export interface CellGroup {
  groupLabel: string;
  cells: string[];
}

// 인원수 괄호 제거 유틸
function strip(s: string) {
  return s.replace(/\([^)]*\)/g, '').trim();
}

function group(label: string, cells: string[]): CellGroup {
  return { groupLabel: strip(label), cells: cells.map(strip) };
}

